    created_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 4. Knowledge Graph Metadata
CREATE TABLE IF NOT EXISTS kg_graph (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(256) NOT NULL,
    description     TEXT,
    owner_id        BIGINT  NOT NULL,
    is_public       BOOLEAN NOT NULL DEFAULT FALSE,
    node_count      INT     NOT NULL DEFAULT 0,
    edge_count      INT     NOT NULL DEFAULT 0,
    status          VARCHAR(32) NOT NULL DEFAULT 'active',
    deleted         SMALLINT NOT NULL DEFAULT 0,
    created_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS kg_build_task (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    graph_id        BIGINT  NOT NULL,
    file_id         BIGINT,
    task_type       VARCHAR(32) NOT NULL,
    status          VARCHAR(32) NOT NULL DEFAULT 'pending',
    progress        INT     NOT NULL DEFAULT 0,
    result          TEXT,
    error_message   TEXT,
    created_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS kg_attachment (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    graph_id        BIGINT  NOT NULL,
    node_id         VARCHAR(256) NOT NULL,
    file_id         BIGINT,
    content_snippet TEXT,
    vector_id       VARCHAR(256),
    created_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS kg_node (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    graph_id        BIGINT  NOT NULL,
    name            VARCHAR(512) NOT NULL,
    node_type       VARCHAR(64)  NOT NULL DEFAULT 'concept',
    description     TEXT,
    parent_id       BIGINT,
    properties      TEXT,
    created_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS kg_edge (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    graph_id        BIGINT  NOT NULL,
    source_node_id  BIGINT  NOT NULL,
    target_node_id  BIGINT  NOT NULL,
    edge_type       VARCHAR(64) NOT NULL DEFAULT 'RELATES_TO',
    properties      TEXT,
    created_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

